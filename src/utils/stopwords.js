/**
 * Removes common English stop words from a given text string.
 * Stop words are removed regardless of case and are only removed if they
 * are at least 2 characters in length.
 * @param {string} text - The text string from which to remove stop words.
 * @returns {string} The text string with stop words removed.
 */
export const removeStopWords = (text) => {
  return text
    .toLowerCase()
    .split(/\s+/)
    .filter((word) => !stopWords.has(word) && word.length > 2)
    .join(' ');
};
